public class variables {
    static void main() {
        int age;
        age = 10;
        age = 11;
        System.out.println(age);

        int totalMarks = 20;
        System.out.println(totalMarks);

        //case sensitive
        int weight = 40;
        int WEIGHT = 69;
        System.out.println(weight);
        System.out.println(WEIGHT);
    }
}
